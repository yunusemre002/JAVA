package J_01_17;

public class J_01_02_03_Degiskenler{
	public static void main(String[] args) {
		
		// Hello world
		System.out.println("Merhabalar nasil gidiyor arabalar");
	
		// Degisikenler
		
		int a = 25; 		// int : 4 byte
		short s = 48; 		// short : 2 byte
		double d = 3.14;	// double : 8 byte
		char c= 'A';		// karakter : 2 byte
		boolean b = true;	// boolean : 1 byte
		String st = "Yunus"; 
		
		System.out.println("The value of A is " + a  );
		System.out.println("short : " + s);
		System.out.println("double : " + d);
		System.out.println("char : " + c);
		System.out.println("boolean : " + b);
		System.out.println("String : " + st);
		
	}
}
